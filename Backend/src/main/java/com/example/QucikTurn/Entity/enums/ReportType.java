package com.example.QucikTurn.Entity.enums;

/**
 * Types of reports that users can submit.
 */
public enum ReportType {
    /**
     * Bug or technical issue in the platform
     */
    BUG,

    /**
     * Issue with a project or contract
     */
    CONTRACT_ISSUE,

    /**
     * User behavior issue or misconduct
     */
    USER_COMPLAINT,

    /**
     * Payment or transaction issue
     */
    PAYMENT_ISSUE,

    /**
     * Other general feedback or issue
     */
    OTHER
}
